//Purpose is to take inputs of marks available and marks achieved in exam
// outputs percentage of mark achieved

#include <stdio.h>

float marks;
float total;

int main(void) {
    
    printf("Enter the total number of marks in the exam: ");
    scanf("%f", &total);
    
    printf("Enter the number of marks the student was awarded: ");
    scanf("%f", &marks);
    
    float percF = ((marks*100)/total);

    printf("The student scored %.0lf%% in this exam.\n", percF);
    
    return 0;
}
